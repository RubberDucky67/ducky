from collections import defaultdict
def build_dictionary():
    word_freq = defaultdict(int)
    dictionary_words = [
        "the", "a", "an", "and", "or", "but",
        "cat", "dog", "bird", "fish",
        "run", "ran", "running", "jump", "jumped",
        "big", "small", "fast", "slow", "red", "blue",
        "happy", "sad", "good", "bad",
        "on", "in", "at", "to", "for", "with",
        "is", "are", "was", "were", "be", "been",
        "i", "you", "he", "she", "it", "we", "they",
        "hello", "hi", "thanks", "please"
    ]
    for word in dictionary_words:
        word_freq[word] = 1
    return word_freq
def generate_candidates_edit1(word):
    candidates = set()
    letters = 'abcdefghijklmnopqrstuvwxyz'
    for i in range(len(word)):
        candidate = word[:i] + word[i+1:]
        if candidate:
            candidates.add(candidate)
    for i in range(len(word)):
        for c in letters:
            candidate = word[:i] + c + word[i+1:]
            candidates.add(candidate)
    for i in range(len(word) - 1):
        candidate = word[:i] + word[i+1] + word[i] + word[i+2:]
        candidates.add(candidate)
    for i in range(len(word) + 1):
        for c in letters:
            candidate = word[:i] + c + word[i:]
            candidates.add(candidate)
    return candidates
def generate_candidates_edit2(word):
    candidates = set()
    edit1 = generate_candidates_edit1(word)
    for w in edit1:
        for c in generate_candidates_edit1(w):
            candidates.add(c)
    return candidates
def get_best_suggestion(word, dictionary):
    if word in dictionary:
        return word
    candidates = generate_candidates_edit1(word)
    valid_candidates = [(candidate, dictionary[candidate])
                        for candidate in candidates
                        if candidate in dictionary]
    if not valid_candidates:
        candidates2 = generate_candidates_edit2(word)
        valid_candidates = [(candidate, dictionary[candidate])
                            for candidate in candidates2
                            if candidate in dictionary]
    if not valid_candidates:
        return word
    max_freq = max(freq for _, freq in valid_candidates)
    best_candidates = [candidate for candidate, freq in valid_candidates
                       if freq == max_freq]
    return min(best_candidates)
def main():
    dictionary = build_dictionary()
    print(f"Dictionary has {len(dictionary)} words.\n")
    n = int(input("Enter number of words to check: "))
    input_words = []
    for i in range(n):
        word = input(f"Enter word {i+1}: ")
        input_words.append(word)
    results = []
    for word in input_words:
        word = word.lower()
        suggestion = get_best_suggestion(word, dictionary)
        results.append(suggestion)
    print("\nSuggestions:")
    for result in results:
        print(result)
if __name__ == "__main__":
    main()

