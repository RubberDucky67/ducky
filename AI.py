"""Similarity Score"""

import math
import re
from collections import Counter
def preprocess(text):
    text = text.lower()
    words = re.findall(r"[a-z]+(?:'[a-z]+)?", text)
    return words
def cosine_similarity(vec1, vec2):
    dot = sum(a * b for a, b in zip(vec1, vec2))
    norm1 = math.sqrt(sum(a * a for a in vec1))
    norm2 = math.sqrt(sum(b * b for b in vec2))
    return dot / (norm1 * norm2) if norm1 and norm2 else 0
n = int(input("How many sentences? "))
sentences = []
for i in range(n):
    sent = input(f"Sentence {i+1}: ")
    sentences.append(sent)
processed = [preprocess(sent) for sent in sentences]
vocab = set()
for sent in processed:
    vocab.update(sent)
vocab = sorted(vocab)
df = {word: sum(1 for sent in processed if word in sent) for word in vocab}
N = len(sentences)
vectors = []
for sent in processed:
    tf = Counter(sent)
    vec = [tf[word] * math.log(N / df[word]) if word in tf else 0 for word in vocab]
    vectors.append(vec)
results = []
for i in range(1, len(sentences)):
    sim = cosine_similarity(vectors[0], vectors[i])
    results.append((i+1, sim))
most_similar = max(results, key=lambda x: x[1])
print(f"\nMost similar to Sentence 1: Sentence {most_similar[0]} (Score: {most_similar[1]:.4f})")