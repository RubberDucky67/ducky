import heapq,math
def h(c,a,b):
    x1,y1=c[a]
    x2,y2=c[b]
    return math.sqrt((x1-x2)**2+(y1-y2)**2)
def best(graph,coords,start,goal):
    q=[(h(coords,start,goal),start,[start],0)]
    v=set()
    while q:
        _,cur,path,cost=heapq.heappop(q)
        if cur in v:
            continue
        v.add(cur)
        if cur==goal:
            print("\nPath:",' -> '.join(path))
            print("Cost:",cost)
            print("\nSteps:\n")
            total=0
            for i in range(len(path)-1):
                a=path[i]
                b=path[i+1]
                for n,w in graph[a]:
                    if n==b:
                        total+=w
                        print(a,"->",b,
                        "| Distance:",w,
                        "| Total:",total,
                        "| Heuristic:",round(h(coords,b,goal),2))
            return
        for n,w in graph[cur]:
            if n not in v:
                heapq.heappush(q,
                (h(coords,n,goal),n,path+[n],cost+w))
    print("No path")
