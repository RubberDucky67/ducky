import copy
goal=[[1,2,3],[4,5,6],[7,8,0]]
def heuristic(state):
    d=0
    for i in range(3):
        for j in range(3):
            if state[i][j]:
                x=(state[i][j]-1)//3
                y=(state[i][j]-1)%3
                d+=abs(x-i)+abs(y-j)
    return d
def neighbors(state):
    for i in range(3):
        for j in range(3):
            if state[i][j]==0:
                x,y=i,j
    a=[]
    for dx,dy in [(-1,0),(1,0),(0,-1),(0,1)]:
        nx,ny=x+dx,y+dy
        if 0<=nx<3 and 0<=ny<3:
            t=copy.deepcopy(state)
            t[x][y],t[nx][ny]=t[nx][ny],t[x][y]
            a.append(t)
    return a
def hill(state):
    step=0
    while True:
        print("Step:",step)
        for i in state:
            print(i)
        cur=heuristic(state)
        print("Heuristic:",cur)
        print("-"*20)
        if state==goal:
            print("Goal Reached")
            return
        next_state=state
        for i in neighbors(state):
            if heuristic(i)<cur:
                next_state=i
                cur=heuristic(i)
        if next_state==state:
            print("Local Maximum Reached")
            return
        state=next_state
        step+=1
