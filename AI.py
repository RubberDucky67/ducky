"""POS Tagging"""

import sys
import re
def simple_pos_tagger(sentence):
    words = re.findall(r'\b\w+\b|[,\.!?;:]', sentence)
    word_tags = {
        # Determiners
        'the': 'DT', 'a': 'DT', 'an': 'DT',
        # Conjunctions
        'and': 'CC', 'or': 'CC', 'but': 'CC', 'so': 'CC', 'for': 'CC',
        # Prepositions
        'in': 'IN', 'on': 'IN', 'at': 'IN', 'to': 'TO', 'for': 'IN',
        'with': 'IN', 'by': 'IN', 'of': 'IN', 'from': 'IN',
        # Verbs (common)
        'is': 'VBZ', 'are': 'VBP', 'was': 'VBD', 'were': 'VBD',
        'am': 'VBP', 'be': 'VB', 'been': 'VBN', 'being': 'VBG',
        'run': 'VB', 'ran': 'VBD', 'running': 'VBG',
        'sit': 'VB', 'sat': 'VBD', 'sitting': 'VBG',
        # Pronouns
        'i': 'PRP', 'you': 'PRP', 'he': 'PRP', 'she': 'PRP', 'it': 'PRP',
        'we': 'PRP', 'they': 'PRP', 'me': 'PRP', 'him': 'PRP', 'her': 'PRP',
        'my': 'PRP$', 'your': 'PRP$', 'his': 'PRP$', 'her': 'PRP$',
        # Adjectives
        'big': 'JJ', 'small': 'JJ', 'fast': 'JJ', 'slow': 'JJ',
        'happy': 'JJ', 'sad': 'JJ', 'good': 'JJ', 'bad': 'JJ',
        # Adverbs
        'fast': 'RB', 'quickly': 'RB', 'slowly': 'RB', 'well': 'RB'
    }
    tagged = []
    for word in words:
        if word in [',', '.', '!', '?', ';', ':']:
            tagged.append(f"{word}/{word}")
            continue
        word_lower = word.lower()
        if word_lower in word_tags:
            tag = word_tags[word_lower]
        elif word.endswith('ing'):
            tag = 'VBG'
        elif word.endswith('ed'):
            tag = 'VBD'
        elif word.endswith('ly'):
            tag = 'RB'
        elif word.endswith('s') and not word.endswith('ss'):
            tag = 'NNS'
        elif word.endswith('er') or word.endswith('est'):
            tag = 'JJ'
        elif word[0].isupper() and len(word) > 1:
            tag = 'NNP'
        else:
            tag = 'NN'
        tagged.append(f"{word}/{tag}")
    return " ".join(tagged)
def main():
    sentence = input().strip()
    result = simple_pos_tagger(sentence)
    print(result)
if __name__ == "__main__":
    main()

